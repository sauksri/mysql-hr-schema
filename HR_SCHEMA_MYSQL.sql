create table EMPLOYEES
(
    EMPLOYEE_ID    INT         not null primary key comment 'Primary key of employees table.',
    FIRST_NAME     VARCHAR(20) COMMENT 'First name of the employee. A not null column.',
    LAST_NAME      VARCHAR(25) not null
        constraint EMP_LAST_NAME_NN
        check (`LAST_NAME` IS NOT NULL) COMMENT 'Last name of the employee. A not null column.',
    EMAIL          VARCHAR(25) not null unique COMMENT 'Email id of the employee',
    PHONE_NUMBER   VARCHAR(20) COMMENT 'Phone number of the employee; includes country code and area code',
    HIRE_DATE      DATETIME    not null
        constraint EMP_HIRE_DATE_NN
        check (`HIRE_DATE` IS NOT NULL) COMMENT 'Date when the employee started on this job. A not null column.',
    JOB_ID         VARCHAR(10) not null COMMENT 'Current job of the employee; foreign key to job_id column of the jobs table. A not null column.',
    foreign key (JOB_ID) references JOBS (JOB_ID),

    SALARY         DECIMAL(8, 2)
        check (salary > 0) COMMENT 'Monthly salary of the employee. Must be greater
than zero (enforced by constraint emp_salary_min)',
    COMMISSION_PCT DECIMAL(2, 2) COMMENT 'Commission percentage of the employee; Only employees in sales
department elgible for commission percentage',
    MANAGER_ID     INT COMMENT 'Manager id of the employee; has same domain as manager_id in
departments table. Foreign key to employee_id column of employees table.
(useful for reflexive joins and CONNECT BY query)',
    foreign key (MANAGER_ID) references EMPLOYEES (EMPLOYEE_ID),
    DEPARTMENT_ID  SMALLINT COMMENT 'Department id where employee works; foreign key to department_id
column of the departments table',
    FOREIGN KEY (DEPARTMENT_ID)
        references DEPARTMENTS (DEPARTMENT_ID)
)
;

alter table EMPLOYEES
    comment 'employees table. Contains 107 rows. References with departments,
jobs, job_history tables. Contains a self reference.'
